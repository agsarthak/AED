    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        
        
        
       try{
           int qty = Integer.parseInt(qtyTb.getText());
           Drug drug = (Drug) drugCb.getSelectedItem();
        if(drugCb.getItemCount()==0 ) {
             JOptionPane.showMessageDialog(null, "Please enter a Drug Catalog", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        else {
       Inventory inventory = store.getInventoryCatalog().addInventory();
       
       inventory.setQty(Integer.parseInt(qtyTb.getText()));
       inventory.setDrug(drug);
       inventory.setExpirationDate(expirationTb.getText());
       inventory.setSerialNumber(serialTb.getText());
        
       if(inventory.getQty() <5){
           inventory.setStockStatus("Low stock !");
       } else{
           inventory.setStockStatus("Optimum stock !");
       }
        JOptionPane.showMessageDialog(null, "Added Successfully", "Created Successfully", JOptionPane.INFORMATION_MESSAGE);
        refreshTable();
       }
       }
       catch(Exception e){
           JOptionPane.showMessageDialog(null, "Qty should be number", "Warning!!", JOptionPane.WARNING_MESSAGE);
       }
       
        
        
    }//GEN-LAST:event_updateBtnActionPerformed

    public void refreshDrugCb() {
        drugCb.removeAllItems();
        for(Pharmacy p : pharmacyDirectory.getPharmacyList()) {
           for(Drug d : p.getDrugCatalog().getDrugList()) {
           drugCb.addItem(d);
           }
         }
    }
    
    public void refreshTable() {
        int rowCount = inventoryTable.getRowCount();
        DefaultTableModel model = (DefaultTableModel)inventoryTable.getModel();
        for(int i=rowCount-1;i>=0;i--) {
            model.removeRow(i);
        }
        for (Inventory i : store.getInventoryCatalog().getInventoryList()) {
            Object row[] = new Object[5];
            row[0] = i.getDrug();
            row[1] = i.getQty();
            row[2] = i.getExpirationDate();
            row[3] = i.getSerialNumber();
            row[4] = i.getStockStatus();
            model.addRow(row);
        }
    }

    
    
    
