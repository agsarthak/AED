    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        
        if(drugNameTb.getText().length()==0){
            JOptionPane.showMessageDialog(null, "Please enter Drug Name", "Warning !!", JOptionPane.WARNING_MESSAGE);
        }
        else{
        
        Drug drug = pharmacy.getDrugCatalog().addDrug();
        drug.setDrugName(drugNameTb.getText());
        drug.setManufacturingDate(manufacturerTb.getText());
        drug.setListPrice(priceTb.getText());
        drug.setDescription(descriptionTb.getText());
        drug.setDrugType(typeTb.getText());
        drug.setDrugMake(drugMakeTb.getText());
        drug.setMainSalt(mainSaltTb.getText());
        drug.setUse(useTb.getText());
        drug.setWeight(weightTb.getText());
        drug.setComposition(compositionTb.getText());
        
        JOptionPane.showMessageDialog(null, "Drug entry created Successfully", "Created Successfully", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_addBtnActionPerformed

    private void backBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_backBtnActionPerformed
        userProcessContainer.remove(this);
        Component[] componentArray = userProcessContainer.getComponents();
        Component component = componentArray[componentArray.length - 1];
        ManageDrugCatalogJPanel manageDrugCatalogJPanel = (ManageDrugCatalogJPanel) component;
        manageDrugCatalogJPanel.refreshTable();
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.previous(userProcessContainer);
    }//GEN-LAST:event_backBtnActionPerformed


