    public void populateOrganizationComboBox(){
        comboOrganization.removeAllItems();
        
        for (Organization organization : organizationDirectory.getOrganizationList()){
            comboOrganization.addItem(organization);
        }
    }
    
    private void populateTable(OrganizationDirectory organizationDirectory){
        DefaultTableModel model = (DefaultTableModel) tableEmployee.getModel();
        
        model.setRowCount(0);
       for(Organization organization : organizationDirectory.getOrganizationList()){
           for (Employee employee : organization.getEmployeeDirectory().getEmployeeList()){
            Object[] row = new Object[3];
            row[0] = organization.getName();
            row[1] = employee.getId();
            row[2] = employee.getName();
            model.addRow(row);
        }
       }
    }
