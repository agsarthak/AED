    private void btnCreateEmpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateEmpActionPerformed
        Organization organization = (Organization) comboOrganization.getSelectedItem();
        String name = textName.getText();
        if (name.length() == 0) {
            JOptionPane.showMessageDialog(null, "Null values not allowed", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            if (organization.getEmployeeDirectory().getEmployeeList().size() > 0) {
                for (Employee employee : organization.getEmployeeDirectory().getEmployeeList()) {
                    if (employee.getName().equals(name)) {
                        JOptionPane.showMessageDialog(null, "Name already exists", "Error", JOptionPane.ERROR_MESSAGE);
                    } else {
                        organization.getEmployeeDirectory().createEmployee(name);
                        if (organization != null) {
                            populateTable(organizationDirectory);
                        }
                        return;
                    }
                }
            } else {
                organization.getEmployeeDirectory().createEmployee(name);
                if (organization != null) {
                    populateTable(organizationDirectory);
                }
            }
        }
    }//GEN-LAST:event_btnCreateEmpActionPerformed

