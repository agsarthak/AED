    private void createBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createBtnActionPerformed
            patient.setAge(Integer.parseInt(ageTb.getText()));    
            patient.setPrimaryDocName(primaryDoctorNameTb.getText());
            patient.setPreferredPharmacy(preferredPharmacyTb.getText());
            
            try {
            float respiratoryRate = Float.parseFloat(respiratoryTb.getText());
            float heartRate = Float.parseFloat(heartRateTb.getText());
            float bp = Float.parseFloat(bloodPressureTb.getText());
            float weight = Float.parseFloat(weightTb.getText());
        
            VitalSigns vs = vitalSignHistory.addVitalSign();
            vs.setRespiratoryRate(respiratoryRate);
            vs.setHeartRate(heartRate);
            vs.setBp(bp);
            vs.setWeight(weight);
            vs.setDate(dateTb.getText());
        
        JOptionPane.showMessageDialog(null, "Vital Sign has been successfully recorded.", "Information", JOptionPane.INFORMATION_MESSAGE);
        resetFields();
    }
        catch(Exception e) {
            System.out.println("Error!");
        JOptionPane.showMessageDialog(null, 
                                      "Please enter valid values", 
                                      "Warning", 
                                      JOptionPane.WARNING_MESSAGE );
        }
            
            
            
    }//GEN-LAST:event_createBtnActionPerformed

    private void backBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_backBtnActionPerformed
        userProcessContainer.remove(this);
        Component[] componentArray = userProcessContainer.getComponents();
        Component component = componentArray[componentArray.length - 1];
        CreatePersonJPanel createPersonJPanel = (CreatePersonJPanel) component;
        createPersonJPanel.refreshTable();
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.previous(userProcessContainer);
    }//GEN-LAST:event_backBtnActionPerformed

    private void viewBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewBtnActionPerformed
        ViewProfileJPanel vprojp = new ViewProfileJPanel(userProcessContainer, vitalSignHistory, person, patient);
        userProcessContainer.add("CreatePatientJPanel", vprojp);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
        
    }//GEN-LAST:event_viewBtnActionPerformed

