    private void removeBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeBtnActionPerformed
           int row = salespersonTable.getSelectedRow();
        if(row<0){
            JOptionPane.showMessageDialog(null, "Please select a row!!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        Salesperson sp = (Salesperson) salespersonTable.getValueAt(row, 0);
        salespersonDirectory.removeSalesperson(sp);
        refreshTable();
    }//GEN-LAST:event_removeBtnActionPerformed

    private void viewSalespersonBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewSalespersonBtnActionPerformed
        int row = salespersonTable.getSelectedRow();
        if(row<0){
            JOptionPane.showMessageDialog(null, "Please select a row!!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        Salesperson salesperson = (Salesperson) salespersonTable.getValueAt(row,0);
        ViewSalesperson vsp = new ViewSalesperson(userProcessContainer, salesperson);
        userProcessContainer.add("ViewSalesperson", vsp);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
    }//GEN-LAST:event_viewSalespersonBtnActionPerformed


