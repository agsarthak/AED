    private void viewSalesPersonJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewSalesPersonJButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow= SalesPersonsJTable.getSelectedRow();
        if(selectedRow<0)
        {
            JOptionPane.showMessageDialog(this,"Please select a Customer.","Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        SalesPerson salesPerson=(SalesPerson) SalesPersonsJTable.getValueAt(selectedRow, 0);
        ViewSalesPersonDetailsJPanel vspjp= new ViewSalesPersonDetailsJPanel(userProcessContainer,salesPerson);
        userProcessContainer.add("ViewSalesPersonDetailsJPanel", vspjp);
        CardLayout layout = (CardLayout)userProcessContainer.getLayout();
        layout.next(userProcessContainer);

    }//GEN-LAST:event_viewSalesPersonJButtonActionPerformed


