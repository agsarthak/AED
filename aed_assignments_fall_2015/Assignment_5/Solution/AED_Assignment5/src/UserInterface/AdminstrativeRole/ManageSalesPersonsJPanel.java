    private void populateSalesPersonTable()
    {
        DefaultTableModel dtm= (DefaultTableModel) SalesPersonsJTable.getModel();
        dtm.setRowCount(0);
        
        for(SalesPerson salesPerson:salesPersonDirectory.getSalesPersonDirectory())
        {
            Object row[]= new Object[4];
            row[0]=salesPerson;
            row[1]=salesPerson.getSalesPersonId();
            row[2]= salesPerson.getFloorCommission();
            row[3]= salesPerson.getCeilCommission();
            dtm.addRow(row);
        }
    }

